
public class Main 
{
	// This is a comment
	
	/*
	 * This is a multi-line comment
	 */
	
	//if you type sysout and the hit ctrl + space + enter it types out System.out.println("");
	
	//anything preceding a backslash is the beginning of an escape sequence 
	public static void main(String[] args) 
	{
		//This will make a new line using \n
		System.out.println("I love soccer\n");
		System.out.println("I want to be a soccer player when I grow up\n");
		
		//This will make a tab in the front using \t
		System.out.println("\tI love soccer");
		System.out.println("I want to be a soccer player when I grow up");

		//This will make a quotes using \"
		System.out.println("\"I love soccer\"");
		System.out.println("I want to be a soccer player when I grow up");
	
		//This will make a backslash using \\
		System.out.println("\\I love soccer\\");
		System.out.println("I want to be a soccer player when I grow up");

		
	}
}
